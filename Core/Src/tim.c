/**
  * @brief  呼吸灯渐变
  * @param  none
  * @retval none 
		pwm计算 
		(Period+ 1) * (Prescaler + 1) /主频
		时间计算
		T(S) = 1/f(HZ)
  */
void PwmLed(void){
  static u16 pwm_time = 0;
  static u8 pwm_flag = 0; 
    if(pwm_flag == 0){
         pwm_time++; 
        if(pwm_time >= 3000)
            pwm_flag = 1; 
    }
    else if(pwm_flag == 1){
        pwm_time--;
        if(pwm_time <=0)
            pwm_flag = 0; 
    } 
		__HAL_TIM_SET_COMPARE(&htim14, TIM_CHANNEL_1,  pwm_time); 
}
